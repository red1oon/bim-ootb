function _rwGuid(prefix) {
  _rwGuidCounter++;
  return RW_PREFIX + prefix + '-' + String(_rwGuidCounter).padStart(4, '0');
}

function _rwInsertElement(db, guid, ifcClass, name, building, storey, disc, rgba, cx, cy, cz, bx, by, bz) {
  try {
    db.run(
      "INSERT OR IGNORE INTO elements_meta (guid, ifc_class, element_name, building, storey, discipline, material_rgba) " +
      "VALUES (?, ?, ?, ?, ?, ?, ?)",
      [guid, ifcClass, name, building, storey, disc, rgba]
    );
    db.run(
      "INSERT OR IGNORE INTO element_transforms (guid, center_x, center_y, center_z, " +
      "rotation_x, rotation_y, rotation_z, bbox_x, bbox_y, bbox_z) " +
      "VALUES (?, ?, ?, ?, 0, 0, 0, ?, ?, ?)",
      [guid, cx, cy, cz, bx, by, bz]
    );
  } catch (e) {
    console.warn('§RW_INSERT_FAIL guid=' + guid + ' err=' + e.message);
  }
}


// ═══════════════════════════════════════════════════════════════
// IndexedDB cache for mep_rw.db
// ═══════════════════════════════════════════════════════════════

function _rwFetchCached(url) {
  return new Promise(function(resolve, reject) {
    var req = indexedDB.open('bim_ootb_cache', 1);
    req.onupgradeneeded = function() { req.result.createObjectStore('dbs'); };
    req.onsuccess = function() {
      var idb = req.result;
      var tx = idb.transaction('dbs', 'readonly');
      var get = tx.objectStore('dbs').get('rw_' + url);
      get.onsuccess = function() {
        if (get.result) {
          console.log('§RW_CACHE_HIT ' + url);
          resolve(get.result);
        } else {
          fetch(url).then(function(resp) {
            if (!resp.ok) throw new Error('HTTP ' + resp.status);
            return resp.arrayBuffer();
          }).then(function(buf) {
            try {
              var tx2 = idb.transaction('dbs', 'readwrite');
              tx2.objectStore('dbs').put(buf, 'rw_' + url);
            } catch (e) { /* cache write fail — continue */ }
            console.log('§RW_CACHE_WRITE ' + url + ' bytes=' + buf.byteLength);
            resolve(buf);
          }).catch(reject);
        }
      };
      get.onerror = function() {
        fetch(url).then(function(r) { return r.arrayBuffer(); }).then(resolve).catch(reject);
      };
    };
    req.onerror = function() {
      fetch(url).then(function(r) { return r.arrayBuffer(); }).then(resolve).catch(reject);
    };
  });
}
