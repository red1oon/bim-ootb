/**
 * Initialise RouteWalker: fetch mep_rw.db, open as sql.js instance.
 * Call once at page load. Caches in IndexedDB (same pattern as building DB).
 * @param {object} SQL - initSqlJs result (sql.js factory)
 * @param {string} [baseUrl] - base URL for mep_rw.db (default: relative)
 * @returns {Promise<boolean>} true if ready
 */
async function rwInit(SQL, baseUrl) {
  if (_rwReady) return true;
  var url = (baseUrl || '') + MEP_RW_DB_URL;
  try {
    var buf = await _rwFetchCached(url);
    _rwDb = new SQL.Database(new Uint8Array(buf));
    _rwReady = true;
    var counts = _rwDb.exec(
      "SELECT (SELECT COUNT(*) FROM ad_space_type_mep_bom) as bom, " +
      "(SELECT COUNT(*) FROM ad_mep_pattern) as pat, " +
      "(SELECT COUNT(*) FROM ad_mep_anchor) as anc, " +
      "(SELECT COUNT(*) FROM ad_placement_offset) as off, " +
      "(SELECT COUNT(*) FROM _shim_attributes) as shim"
    );
    var r = counts[0].values[0];
    console.log('§RW_INIT mep_rw.db loaded bom=' + r[0] + ' patterns=' + r[1] +
                ' anchors=' + r[2] + ' offsets=' + r[3] + ' shims=' + r[4]);
    return true;
  } catch (e) {
    console.warn('§RW_INIT_FAIL ' + e.message);
    return false;
  }
}

/**
 * Run RouteWalker on a building DB.
 * Path A: building has anchors in mep_rw.db → use Java-style pattern walk
 * Path B: building has no anchors → generate from room BOM recipes + placement offsets
 *
 * @param {object} buildingDb - sql.js Database instance (building's extracted DB)
 * @param {string} buildingName - building name (matches source_building in anchors)
 * @param {object} [opts] - { rooms: [{id,type,storey,bbox}], forceRegen: false }
 * @returns {object} { fixtures, pipes, clashSkipped, total }
 */
function rwWalk(buildingDb, buildingName, opts) {
  if (!_rwReady) { console.warn('§RW_WALK_FAIL not initialised'); return null; }
  opts = opts || {};
  _rwGuidCounter = 0;

  // Resolve actual building name from DB (may differ from ERP anchor key)
  var dbBldName = buildingName;
  try {
    var bldRow = buildingDb.exec("SELECT DISTINCT building FROM elements_meta LIMIT 1");
    if (bldRow.length && bldRow[0].values.length) dbBldName = bldRow[0].values[0][0];
  } catch (e) { /* use passed name */ }

  // Clean previous RW2D- elements (idempotent re-run)
  try {
    buildingDb.run("DELETE FROM elements_meta WHERE guid LIKE 'RW2D-%'");
    buildingDb.run("DELETE FROM element_transforms WHERE guid LIKE 'RW2D-%'");
  } catch (e) { /* tables may not exist yet */ }

  // Load ARC envelope from building DB for clash checking
  var arcEnvelope = _rwLoadArcEnvelope(buildingDb);

  // Check if this building has pre-mined anchors (try exact name, then fuzzy)
  var anchorKey = _rwFindAnchorKey(buildingName, dbBldName);
  var hasAnchors = anchorKey !== null;

  var result = { fixtures: 0, pipes: 0, clashSkipped: 0, total: 0 };

  if (hasAnchors && !opts.forceRegen) {
    // Path A: pattern walk using pre-mined anchors (Java port)
    console.log('§RW_PATH_A building=' + buildingName + ' anchorKey=' + anchorKey);
    var patResult = _rwPatternWalk(buildingDb, anchorKey, arcEnvelope);
    result.pipes = patResult.cwLines + patResult.spLines;
    result.clashSkipped = patResult.clashSkipped;
  }

  // Path B: room-based BOM placement (always runs — adds fixtures)
  // Uses dbBldName for DB queries (the actual building column value)
  var rooms = opts.rooms || _rwDetectRooms(buildingDb, dbBldName);
  if (rooms.length > 0) {
    console.log('§RW_PATH_B building=' + dbBldName + ' rooms=' + rooms.length);
    var bomResult = _rwPlaceFromBOM(buildingDb, dbBldName, rooms, arcEnvelope);
    result.fixtures += bomResult.placed;

    // If no pre-mined anchors, also generate pipe runs from placed fixtures
    if (!hasAnchors || opts.forceRegen) {
      var pipeResult = _rwConnectFixtures(buildingDb, dbBldName, arcEnvelope);
      result.pipes += pipeResult.pipes;
      result.clashSkipped += pipeResult.clashSkipped;
    }
  }

  result.total = result.fixtures + result.pipes;
  console.log('§RW_WALK building=' + buildingName + ' fixtures=' + result.fixtures +
              ' pipes=' + result.pipes + ' clashSkipped=' + result.clashSkipped +
              ' total=' + result.total);
  return result;
}


/**
 * Find anchor key in mep_rw.db for this building.
 * Tries exact match, then fuzzy (DB name contains anchor key or vice versa).
 * e.g. DB has 'Ifc2x3_Duplex_Architecture', anchors keyed as 'Duplex' → match.
 */
function _rwFindAnchorKey(buildingName, dbBldName) {
  // Get all source_building values from anchors
  var rows = _rwDb.exec("SELECT DISTINCT source_building FROM ad_mep_anchor");
  if (!rows.length) return null;
  var keys = rows[0].values.map(function(v) { return v[0]; });

  // Exact match
  if (keys.indexOf(buildingName) >= 0) return buildingName;
  if (keys.indexOf(dbBldName) >= 0) return dbBldName;

  // Fuzzy: anchor key is substring of DB name or vice versa
  var candidates = [buildingName, dbBldName];
  for (var c = 0; c < candidates.length; c++) {
    var name = candidates[c].toUpperCase();
    for (var i = 0; i < keys.length; i++) {
      var key = keys[i].toUpperCase();
      if (name.indexOf(key) >= 0 || key.indexOf(name) >= 0) {
        console.log('§RW_ANCHOR_MATCH fuzzy "' + candidates[c] + '" → "' + keys[i] + '"');
        return keys[i];
      }
    }
  }
  return null;
}

// ═══════════════════════════════════════════════════════════════
// PATH A: Pattern walk (Java RouteWalker port)
// ═══════════════════════════════════════════════════════════════

function _rwPatternWalk(buildingDb, buildingName, arcEnvelope) {
  var cwSteps = _rwLoadPatternSteps('CW', buildingName);
  var spSteps = _rwLoadPatternSteps('SP', buildingName);
  var allAnchors = _rwLoadAnchors(buildingName);

  if (!allAnchors.length) {
    console.warn('§RW_WARN no anchors for ' + buildingName);
    return { cwLines: 0, spLines: 0, clashSkipped: 0 };
  }

  var cwResult = cwSteps.length ? _rwApplyPattern(buildingDb, 'CW', cwSteps, allAnchors, arcEnvelope, buildingName) : { emitted: 0, clashSkipped: 0 };
  var spResult = spSteps.length ? _rwApplyPattern(buildingDb, 'SP', spSteps, allAnchors, arcEnvelope, buildingName) : { emitted: 0, clashSkipped: 0 };

  console.log('§RW_PATTERN ' + buildingName + ': CW=' + cwResult.emitted +
              ' SP=' + spResult.emitted + ' clashSkipped=' + (cwResult.clashSkipped + spResult.clashSkipped));
  return {
    cwLines: cwResult.emitted,
    spLines: spResult.emitted,
    clashSkipped: cwResult.clashSkipped + spResult.clashSkipped
  };
}

// A1. Load pattern steps from mep_rw.db
function _rwLoadPatternSteps(discipline, buildingType) {
  var rows = _rwDb.exec(
    "SELECT pattern_id, discipline, building_type, sequence, from_node_type, to_node_type, " +
    "direction_axis, piece_type, offset_rule, COALESCE(gradient, 0) " +
    "FROM ad_mep_pattern WHERE discipline = '" + discipline + "' " +
    "ORDER BY CASE WHEN building_type = '" + buildingType.replace(/'/g, "''") + "' THEN 0 ELSE 1 END, sequence"
  );
  if (!rows.length || !rows[0].values.length) return [];
  var selectedId = rows[0].values[0][0];
  var steps = [];
  for (var i = 0; i < rows[0].values.length; i++) {
    var v = rows[0].values[i];
    if (v[0] !== selectedId) break; // only first (best) pattern
    steps.push({
      patternId: v[0], discipline: v[1], buildingType: v[2], sequence: v[3],
      fromNodeType: v[4], toNodeType: v[5], directionAxis: v[6],
      pieceType: v[7], offsetRule: v[8], gradient: v[9]
    });
  }
  if (steps.length) console.log('§RW_PATTERN_LOADED ' + discipline + ' pattern=' + selectedId + ' steps=' + steps.length);
  return steps;
}

// A2. Load anchors from mep_rw.db
function _rwLoadAnchors(buildingName) {
  var rows = _rwDb.exec(
    "SELECT anchor_id, anchor_type, x_m, y_m, z_m, storey " +
    "FROM ad_mep_anchor WHERE source_building = '" + buildingName.replace(/'/g, "''") + "'"
  );
  if (!rows.length) return [];
  return rows[0].values.map(function(v) {
    return { anchorId: v[0], anchorType: v[1], x: v[2], y: v[3], z: v[4], storey: v[5] || 'Unknown' };
  });
}

// Node type mapping (same logic as Java)
function _rwToNodeType(anchorType, discipline) {
  if (anchorType === 'METER') return discipline === 'SP' ? 'STACK' : 'METER';
  if (anchorType === 'FIXTURE') return 'FIXTURE';
  if (anchorType === 'VALVE') return 'VALVE';
  return 'JUNCTION';
}

// A3. Apply pattern — pair anchors, check clash, emit pipe segments
function _rwApplyPattern(buildingDb, discipline, steps, allAnchors, arcEnvelope, buildingName) {
  // Group anchors by storey
  var byStorey = {};
  allAnchors.forEach(function(a) {
    var s = a.storey || 'Unknown';
    if (!byStorey[s]) byStorey[s] = [];
    byStorey[s].push(a);
  });

  var emitted = 0, clashSkipped = 0;

  Object.keys(byStorey).forEach(function(storey) {
    var storeyAnchors = byStorey[storey];
    var usedIds = {};

    steps.forEach(function(step) {
      var fromCandidates = [], toCandidates = [];
      storeyAnchors.forEach(function(a) {
        var nt = _rwToNodeType(a.anchorType, discipline);
        if (nt === step.fromNodeType) fromCandidates.push(a);
        if (nt === step.toNodeType && !usedIds[a.anchorId]) toCandidates.push(a);
      });

      if (!fromCandidates.length || !toCandidates.length) return;

      fromCandidates.forEach(function(from) {
        // Find nearest unmatched to-anchor
        var nearest = null, minDist = Infinity;
        toCandidates.forEach(function(to) {
          if (usedIds[to.anchorId]) return;
          var dx = to.x - from.x, dy = to.y - from.y;
          var dist = Math.sqrt(dx * dx + dy * dy);
          if (dist < minDist) { minDist = dist; nearest = to; }
        });
        if (!nearest) return;

        // Sanity guard: skip distance=0 or >50m
        if (minDist === 0 || minDist > RW_MAX_PAIR_DIST) return;

        var dx = nearest.x - from.x;
        var dy = nearest.y - from.y;
        var dz = nearest.z - from.z;

        // Gradient steps: enforce slope
        if (step.offsetRule === 'GRADIENT' && step.gradient > 0) {
          var horiz = Math.sqrt(dx * dx + dy * dy);
          dz = step.gradient * horiz;
        }

        var pipeLen = Math.sqrt(dx * dx + dy * dy + dz * dz);
        var midX = (from.x + nearest.x) / 2;
        var midY = (from.y + nearest.y) / 2;
        var midZ = (from.z + nearest.z) / 2;

        // ARC envelope clash check
        if (_rwClashesWithArc(midX, midY, midZ, RW_PIPE_CROSS / 1000, RW_PIPE_CROSS / 1000, pipeLen, arcEnvelope)) {
          clashSkipped++;
          return;
        }

        // Emit pipe segment
        var info = discipline === 'CW' ? RW_IFC_MAP.PIPE_CW : RW_IFC_MAP.PIPE_SP;
        var guid = _rwGuid(discipline + '_PIPE');
        _rwInsertElement(buildingDb, guid, info.cls,
          discipline + ' Pipe ' + step.directionAxis + ' ' + storey,
          buildingName, storey, info.disc, info.rgba,
          midX, midY, midZ,
          Math.abs(dx) || RW_PIPE_CROSS / 1000,
          Math.abs(dy) || RW_PIPE_CROSS / 1000,
          Math.abs(dz) || RW_PIPE_CROSS / 1000
        );
        emitted++;
        usedIds[nearest.anchorId] = true;
      });
    });
  });

  return { emitted: emitted, clashSkipped: clashSkipped };
}

// A4. ARC envelope loader (from building DB)
function _rwLoadArcEnvelope(buildingDb) {
  var boxes = [];
  try {
    var rows = buildingDb.exec(
      "SELECT t.center_x, t.center_y, t.center_z, t.bbox_x, t.bbox_y, t.bbox_z " +
      "FROM elements_meta m JOIN element_transforms t ON m.guid = t.guid " +
      "WHERE m.discipline = 'ARC' AND m.ifc_class IN ('IfcWall','IfcWallStandardCase','IfcSlab','IfcRoof','IfcCovering')"
    );
    if (rows.length) {
      rows[0].values.forEach(function(v) {
        if (v[3] > 0 && v[4] > 0 && v[5] > 0) {
          boxes.push({ cx: v[0], cy: v[1], cz: v[2], w: v[3], d: v[4], h: v[5] });
        }
      });
    }
  } catch (e) { /* no ARC elements — no clash check */ }
  console.log('§RW_ARC_ENVELOPE boxes=' + boxes.length);
  return boxes;
}

// AABB overlap check (same as Java)
function _rwAabbOverlap(c1, s1, c2, s2, tol) {
  return Math.abs(c1 - c2) < (s1 / 2 + s2 / 2 + tol);
}

function _rwClashesWithArc(px, py, pz, pw, pd, ph, arcs) {
  for (var i = 0; i < arcs.length; i++) {
    var a = arcs[i];
    if (_rwAabbOverlap(px, pw, a.cx, a.w, RW_ARC_CLASH_TOL) &&
        _rwAabbOverlap(py, pd, a.cy, a.d, RW_ARC_CLASH_TOL) &&
        _rwAabbOverlap(pz, ph, a.cz, a.h, RW_ARC_CLASH_TOL)) {
      return true;
    }
  }
  return false;
}


