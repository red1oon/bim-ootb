/**
 * Detect rooms from building DB.
 * Uses IfcSpace elements if present, falls back to storey-level generic rooms.
 */
function _rwDetectRooms(buildingDb, buildingName) {
  var rooms = [];

  // Try IfcSpace first
  try {
    var spaceRows = buildingDb.exec(
      "SELECT m.guid, m.element_name, m.storey, " +
      "t.center_x, t.center_y, t.center_z, t.bbox_x, t.bbox_y, t.bbox_z " +
      "FROM elements_meta m JOIN element_transforms t ON m.guid = t.guid " +
      "WHERE m.ifc_class = 'IfcSpace' AND m.building = '" + buildingName.replace(/'/g, "''") + "'"
    );
    if (spaceRows.length && spaceRows[0].values.length) {
      spaceRows[0].values.forEach(function(v) {
        rooms.push({
          id: v[0], name: v[1], storey: v[2],
          type: _rwClassifyRoom(v[1], v[6] * v[7]),
          cx: v[3], cy: v[4], cz: v[5],
          bx: v[6], by: v[7], bz: v[8]
        });
      });
      console.log('§RW_ROOMS source=IfcSpace count=' + rooms.length);
      return rooms;
    }
  } catch (e) { /* no IfcSpace */ }

  // Fallback: one generic room per storey from wall envelope
  try {
    var storeyRows = buildingDb.exec(
      "SELECT m.storey, " +
      "AVG(t.center_x), AVG(t.center_y), AVG(t.center_z), " +
      "MAX(t.bbox_x), MAX(t.bbox_y), MAX(t.bbox_z), " +
      "COUNT(*) " +
      "FROM elements_meta m JOIN element_transforms t ON m.guid = t.guid " +
      "WHERE m.building = '" + buildingName.replace(/'/g, "''") + "' " +
      "AND m.discipline = 'ARC' " +
      "GROUP BY m.storey"
    );
    if (storeyRows.length) {
      storeyRows[0].values.forEach(function(v) {
        var area = (v[4] || 10) * (v[5] || 10);
        rooms.push({
          id: 'STOREY_' + (v[0] || 'Unknown'),
          name: v[0] || 'Unknown',
          storey: v[0] || 'Unknown',
          type: _rwClassifyRoom(v[0], area),
          cx: v[1], cy: v[2], cz: v[3],
          bx: v[4] || 10, by: v[5] || 10, bz: v[6] || 3
        });
      });
    }
  } catch (e) { /* empty DB */ }

  console.log('§RW_ROOMS source=storey_fallback count=' + rooms.length);
  return rooms;
}

/**
 * Classify room type from name + area, matching ad_space_type values.
 */
function _rwClassifyRoom(name, areaSqm) {
  if (!name) name = '';
  var n = name.toUpperCase();

  // Name-based classification
  if (/TOILET|BATH|WC|SHOWER|RESTROOM/.test(n)) return 'BATHROOM';
  if (/KITCHEN|PANTRY|DAPUR/.test(n)) return 'KITCHEN';
  if (/MASTER.*BED|BED.*MASTER/.test(n)) return 'MASTER_BEDROOM';
  if (/BED|BILIK\s*TIDUR/.test(n)) return 'BEDROOM';
  if (/LIVING|LOUNGE|RUANG\s*TAMU/.test(n)) return 'LIVING';
  if (/DINING|MAKAN/.test(n)) return 'DINING';
  if (/CORRIDOR|HALLWAY|LORONG/.test(n)) return 'CORRIDOR';
  if (/LOBBY/.test(n)) return 'LOBBY';
  if (/OFFICE|PEJABAT/.test(n)) return 'OFFICE';
  if (/STORE|STOR/.test(n)) return 'STORAGE';
  if (/GARAGE|PARKING/.test(n)) return 'GARAGE';
  if (/STAIR/.test(n)) return 'STAIR';
  if (/DEPART|LOUNGE/.test(n)) return 'DEPARTURE_LOUNGE';
  if (/GATE/.test(n)) return 'GATE';
  if (/CONCOURSE/.test(n)) return 'CONCOURSE';
  if (/CLASS/.test(n)) return 'CLASSROOM';
  if (/ASSEMBLY|HALL|DEWAN/.test(n)) return 'ASSEMBLY_HALL';
  if (/CANTEEN|CAFE/.test(n)) return 'CANTEEN';

  // Area-based fallback
  if (areaSqm && areaSqm < 4) return 'BATHROOM';
  if (areaSqm && areaSqm < 12) return 'BEDROOM';
  if (areaSqm && areaSqm < 25) return 'OFFICE';
  if (areaSqm && areaSqm < 50) return 'LIVING';
  return 'GENERIC';
}

/**
 * Place MEP fixtures from BOM recipes into building DB.
 * Reads ad_space_type_mep_bom + ad_placement_offset + _shim_attributes.
 */
function _rwPlaceFromBOM(buildingDb, buildingName, rooms, arcEnvelope) {
  var placed = 0;

  // Load placement offsets (keyed by rule name)
  var offsets = {};
  var offRows = _rwDb.exec("SELECT * FROM ad_placement_offset");
  if (offRows.length) {
    var cols = offRows[0].columns;
    offRows[0].values.forEach(function(v) {
      var o = {};
      cols.forEach(function(c, i) { o[c] = v[i]; });
      offsets[v[0]] = o;
    });
  }

  // Load shim attributes (keyed by product_value)
  var shims = {};
  var shimRows = _rwDb.exec("SELECT * FROM _shim_attributes");
  if (shimRows.length) {
    var scols = shimRows[0].columns;
    shimRows[0].values.forEach(function(v) {
      var s = {};
      scols.forEach(function(c, i) { s[c] = v[i]; });
      shims[v[0]] = s;
    });
  }

  rooms.forEach(function(room) {
    // Look up BOM recipe for this room type
    var bomRows = _rwDb.exec(
      "SELECT mep_product_id, qty_normal, placement_rule, host_surface, anchor_end " +
      "FROM ad_space_type_mep_bom WHERE space_type_id = '" + room.type.replace(/'/g, "''") + "'"
    );
    if (!bomRows.length) return;

    bomRows[0].values.forEach(function(v) {
      var product = v[0];
      var qty = v[1] || 1;
      var placementRule = v[2] || 'AUTO';
      var hostSurface = v[3] || 'WALL';
      var anchorEnd = v[4] || 'PANEL';

      var offset = offsets[placementRule] || offsets['AUTO'];
      if (!offset) return;

      var info = RW_IFC_MAP[product] || RW_IFC_MAP._DEFAULT;

      for (var q = 0; q < qty; q++) {
        // Compute XYZ from room bbox + placement offset
        var pos = _rwComputePosition(room, offset, hostSurface, q, qty);

        // Check ARC clash
        if (_rwClashesWithArc(pos.x, pos.y, pos.z, 0.1, 0.1, 0.1, arcEnvelope)) continue;

        var guid = _rwGuid(product);
        _rwInsertElement(buildingDb, guid, info.cls,
          product + ' ' + room.storey + (qty > 1 ? ' #' + (q + 1) : ''),
          buildingName, room.storey, info.disc, info.rgba,
          pos.x, pos.y, pos.z,
          0.15, 0.15, 0.15  // fixture bbox — small
        );
        placed++;
      }
    });
  });

  console.log('§RW_BOM_PLACE rooms=' + rooms.length + ' fixtures=' + placed);
  return { placed: placed };
}

/**
 * Compute XYZ position from room bbox + placement offset rule.
 * Implements the ad_placement_offset z_rule / x_ref / y_ref logic.
 */
function _rwComputePosition(room, offset, hostSurface, index, total) {
  var cx = room.cx, cy = room.cy, cz = room.cz;
  var hx = (room.bx || 4) / 2, hy = (room.by || 4) / 2, hz = (room.bz || 3) / 2;

  // X position
  var x = cx;
  if (offset.x_ref === 'MIN')       x = cx - hx + (offset.from_edge_x || 0);
  else if (offset.x_ref === 'MAX')  x = cx + hx - (offset.from_edge_x || 0);
  // For WALL_SPACED: distribute along wall
  if (total > 1 && hostSurface === 'WALL') {
    var wallLen = room.bx || 4;
    var spacing = wallLen / (total + 1);
    x = cx - hx + spacing * (index + 1);
  }

  // Y position
  var y = cy;
  if (offset.y_ref === 'MIN')       y = cy - hy + (offset.from_edge_y || 0);
  else if (offset.y_ref === 'MAX')  y = cy + hy - (offset.from_edge_y || 0);

  // Z position
  var z = cz;
  var zOff = offset.z_offset || 0;
  if (offset.z_rule === 'FLOOR')        z = cz - hz + zOff;
  else if (offset.z_rule === 'CEILING') z = cz + hz - zOff;
  else if (offset.z_rule === 'MID')     z = cz + zOff;

  return { x: x, y: y, z: z };
}


